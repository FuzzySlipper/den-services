-- Reset all identity sequences after an import that preserves SQLite integer IDs.
-- This function is intentionally idempotent and should be called by the import
-- tool after COPY/INSERT completes.
create or replace function den_core.reset_identity_sequences_after_import()
returns void
language plpgsql
as $$
declare
    table_name text;
    seq_name text;
begin
    foreach table_name in array array[
        'tasks',
        'task_history',
        'messages',
        'documents',
        'blackboard_entries',
        'review_rounds',
        'review_findings',
        'dispatch_entries',
        'agent_stream_entries',
        'channels',
        'channel_messages',
        'channel_memberships',
        'channel_reactions',
        'desktop_git_snapshots',
        'desktop_diff_snapshots',
        'desktop_session_snapshots',
        'desktop_session_events',
        'consolidation_topics',
        'topic_clip_queue_items',
        'curation_decisions',
        'discussion_threads',
        'discussion_comments',
        'knowledge_entries',
        'knowledge_entry_revisions',
        'knowledge_entry_links',
        'agent_guidance_entries',
        'collaboration_sessions',
        'collaboration_turns',
        'collaboration_segments',
        'collaboration_annotations',
        'collaboration_response_drafts',
        'worker_assignments',
        'worker_checkpoints',
        'checkpoint_responses',
        'worker_no_capacity_requests',
        'orchestrator_leases',
        'capability_invocations',
        'usage_events',
        'pricing_snapshots'
    ]
    loop
        select pg_get_serial_sequence(format('den_core.%I', table_name), 'id') into seq_name;
        if seq_name is not null then
            execute format('select setval(%L, coalesce((select max(id) from den_core.%I), 0) + 1, false)', seq_name, table_name);
        end if;
    end loop;
end;
$$;

comment on function den_core.reset_identity_sequences_after_import() is
  'Call after importing SQLite rows with preserved integer IDs so next generated IDs advance past imported data.';

do $$
begin
    if exists (select 1 from pg_roles where rolname = 'den_core_app') then
        grant usage on schema den_core to den_core_app;
        grant select, insert, update, delete on all tables in schema den_core to den_core_app;
        grant usage, select on all sequences in schema den_core to den_core_app;
        alter default privileges in schema den_core grant select, insert, update, delete on tables to den_core_app;
        alter default privileges in schema den_core grant usage, select on sequences to den_core_app;
    end if;
end $$;
