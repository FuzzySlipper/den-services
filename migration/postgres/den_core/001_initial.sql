    updated_at timestamptz not null default now()
);
create index idx_tasks_project_status on den_core.tasks(project_id, status);
create index idx_tasks_assigned on den_core.tasks(assigned_to);
create index idx_tasks_parent on den_core.tasks(parent_id);

