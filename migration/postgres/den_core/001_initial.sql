-- den_core Phase 0 draft schema for den-core SQLite -> shared Postgres migration.
--
-- This schema is intentionally TEMPORARY. During Phase 0 the existing .NET
-- den-core process remains the only writer and keeps its existing route/MCP
-- surfaces. Later Phase 1+ extraction waves move tables out of den_core into
-- per-domain den_* schemas with their own writer services.
--
-- Source inventory:
--   den-core/src/DenCore/Data/DatabaseInitializer.cs
--   den-core/den-core-postgres-phase-0-plan-2026-06
--   den-core/den-core-sqlite-coupling-audit-2026-06
--
-- Drafting choices:
-- * Keep current table names.
-- * Preserve importable integer IDs via GENERATED BY DEFAULT AS IDENTITY.
-- * Store timestamps as timestamptz and JSON-shaped columns as jsonb.
-- * Do not copy SQLite FTS5 virtual tables. Documents/knowledge use tsvector
--   columns + GIN indexes as the placeholder Postgres FTS shape; #3324 owns
--   final ranking/snippet/query behavior.

create extension if not exists pg_trgm;

comment on schema den_core is
  'Temporary Phase 0 Core-owned schema for den-core SQLite to Postgres migration. Not final den-services domain ownership.';

create table den_core.projects (
    id text primary key,
    name text not null,
    kind text not null default 'project' check (kind in ('project','personal','assistant','knowledge_base','system')),
    visibility text not null default 'normal' check (visibility in ('normal','hidden','archived')),
    owner text,
    root_path text,
    description text,
    settings_json jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table den_core.tasks (
    id bigint generated by default as identity primary key,
    project_id text not null references den_core.projects(id) on delete cascade,
    parent_id bigint references den_core.tasks(id) on delete cascade,
    title text not null,
    description text,
    status text not null default 'planned' check (status in ('planned','in_progress','review','blocked','done','cancelled')),
    priority integer not null default 3 check (priority between 1 and 5),
    assigned_to text,
    tags jsonb,
    blocker_summary text,
    blocker_reason text,
    blocker_attempted_remedies text,
    blocker_suggested_next_step text,
    blocker_requires_human_input boolean not null default false,
