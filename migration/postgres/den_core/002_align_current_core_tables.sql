-- Align Phase 0 den_core draft tables with current den-core SQLite schema
-- surfaces found by the #3319 import/parity dry run.
--
-- These tables were stale in 001_initial.sql because their Core-owned SQLite
-- shapes continued to evolve while the first Postgres draft was being prepared.
-- Phase 0 is still temporary and Core-owned; this migration preserves current
-- table/column names for import parity rather than introducing final domain
-- service ownership.

drop table if exists den_core.checkpoint_responses cascade;
drop table if exists den_core.worker_checkpoints cascade;
drop table if exists den_core.worker_assignments cascade;
drop table if exists den_core.worker_no_capacity_requests cascade;
drop table if exists den_core.orchestrator_leases cascade;
drop table if exists den_core.worker_pool_lanes cascade;
drop table if exists den_core.worker_pool_members cascade;
drop table if exists den_core.capability_invocations cascade;
drop table if exists den_core.capability_definitions cascade;
drop table if exists den_core.usage_events cascade;
drop table if exists den_core.pricing_snapshots cascade;
drop table if exists den_core.collaboration_response_drafts cascade;
drop table if exists den_core.collaboration_annotations cascade;
drop table if exists den_core.collaboration_segments cascade;
drop table if exists den_core.collaboration_turns cascade;
drop table if exists den_core.collaboration_sessions cascade;

create table den_core.capability_definitions (
    capability_id text primary key,
    display_name text not null,
    description text not null default '',
    owner_project_id text references den_core.projects(id) on delete set null,
    implementation_kind text not null default 'registry_only',
    service_endpoint text,
    http_method text not null default 'POST',
    input_schema_ref text,
    output_schema_ref text,
    input_schema_json jsonb,
    output_schema_json jsonb,
    side_effect_level text not null default 'read_only',
    status text not null default 'experimental',
    default_model_json jsonb,
    fallback_models_json jsonb,
    eval_refs_json jsonb,
    timeout_ms integer not null default 30000,
    max_request_bytes integer not null default 10485760,
    metadata_json jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);
create index idx_capability_definitions_status on den_core.capability_definitions(status);
create index idx_capability_definitions_side_effect on den_core.capability_definitions(side_effect_level);
create index idx_capability_definitions_owner on den_core.capability_definitions(owner_project_id);

