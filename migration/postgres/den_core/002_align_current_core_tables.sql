create index idx_cap_invocations_invocation on den_core.capability_invocations(invocation_id);
create index idx_cap_invocations_capability on den_core.capability_invocations(capability_id);
create index idx_cap_invocations_caller_project on den_core.capability_invocations(caller_project_id);
create index idx_cap_invocations_status on den_core.capability_invocations(status);
create index idx_cap_invocations_caller_task on den_core.capability_invocations(caller_task_id) where caller_task_id is not null;

create table den_core.collaboration_sessions (
    id bigint generated by default as identity primary key,
    project_id text not null references den_core.projects(id) on delete cascade,
    task_id bigint references den_core.tasks(id) on delete set null,
    message_id bigint references den_core.messages(id) on delete set null,
    agent_stream_entry_id bigint references den_core.agent_stream_entries(id) on delete set null,
    pi_run_id text,
    pi_session_id text,
    desktop_operator_session_id text,
    title text,
    status text not null default 'active',
    created_by text,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);
create index idx_collaboration_sessions_project_updated on den_core.collaboration_sessions(project_id, updated_at desc, id desc);
create index idx_collaboration_sessions_task_updated on den_core.collaboration_sessions(task_id, updated_at desc, id desc) where task_id is not null;
create index idx_collaboration_sessions_message on den_core.collaboration_sessions(message_id) where message_id is not null;
create index idx_collaboration_sessions_agent_stream on den_core.collaboration_sessions(agent_stream_entry_id) where agent_stream_entry_id is not null;

create table den_core.collaboration_turns (
    id bigint generated by default as identity primary key,
    session_id bigint not null references den_core.collaboration_sessions(id) on delete cascade,
    turn_order integer not null,
    role text,
    source_kind text,
    source_ref text,
    source_label text,
    source_uri text,
    source_context text,
    raw_markdown text not null,
    source_content_hash text not null,
    segmenter_version text not null,
    created_at timestamptz not null default now(),
    unique(session_id, turn_order)
);
create index idx_collaboration_turns_session_order on den_core.collaboration_turns(session_id, turn_order);

create table den_core.collaboration_segments (
    id bigint generated by default as identity primary key,
    turn_id bigint not null references den_core.collaboration_turns(id) on delete cascade,
    sequence_number integer not null,
    segment_hash text not null,
    segment_type text not null,
    raw_markdown text not null,
    text text,
    heading_level integer,
    code_language text,
    created_at timestamptz not null default now(),
    unique(turn_id, sequence_number),
    unique(turn_id, segment_hash)
);
create index idx_collaboration_segments_turn_order on den_core.collaboration_segments(turn_id, sequence_number);

create table den_core.collaboration_annotations (
