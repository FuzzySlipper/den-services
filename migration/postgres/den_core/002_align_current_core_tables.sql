    completed_at timestamptz,
    duration_ms bigint,
    model_provider text,
    model_name text,
    model_version text,
    timings_ms_json jsonb,
    cost_json jsonb,
    output_summary text,
    output_json jsonb,
    output_artifact_refs_json jsonb,
    error_type text,
    error_message text,
    metadata_json jsonb,
