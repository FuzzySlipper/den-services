do $$
begin
    if exists (select 1 from pg_roles where rolname = 'den_core_app') then
        grant select, insert, update, delete on all tables in schema den_core to den_core_app;
        grant usage, select on all sequences in schema den_core to den_core_app;
    end if;
end $$;
